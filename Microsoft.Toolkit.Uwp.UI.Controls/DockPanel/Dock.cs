namespace Microsoft.Toolkit.Uwp.UI.Controls
{
    /// <summary>
    /// Specifies the Dock position of a child element that is inside a <see cref="DockPanel"/>.
    /// </summary>
    public enum Dock
    {
        /// <summary>
        /// Dock left
        /// </summary>
        Left,

        /// <summary>
        /// Dock Top
        /// </summary>
        Top,

        /// <summary>
        /// Dock Right
        /// </summary>
        Right,

        /// <summary>
        /// Dock Bottom
        /// </summary>
        Bottom
    }
}
